package patternProgramming;
public class StarPattern {
	public static void main(String[]args) {
		int n=5;
		int i;
		int j;
		int count=1;
				for(i=1;i<=n;i++) {
					for( j=1;j<=n;j++) {
						
				if(count==10) {
					System.out.println(count);
					count++;
				}
				
			}
					System.out.println();
			System.out.println();
		}
			
	}}
	

	
